colors = ['red', 'green', 'blue', 'yellow', 'orange', 'black', 'white', 'violet', 'grey']
numbers = [1, 4, 4, 7, 2, 3, 9, 5, 4, 9, 2, 1, 8, 6]
print(numbers)

# sorts the list ascending by default.numbers.sort()
numbers.sort()
print(numbers)

# Sort the list in a descending manner
numbers.sort(reverse=True)
print(numbers)

print(colors)

colors.reverse()
print(colors)

colors.sort()
print(colors)

colors.sort(reverse=True)
print(colors)
